#include<stdio.h>
int main(int argc, char const *argv[])
{
    int n,c=0;
    scanf("%d",&n);
    if(n>=1000)
    {
        c=c+(n/1000);
        n=n%1000;
    }
    if(n>=500)
    {
        c=c+(n/500);
        n=n%500;
    }
    if(n>=100)
    {
        c=c+(n/100);
        n=n%100;
    }
    if(n>=50)
    {
        c=c+(n/50);
        n=n%50;
    }
    if(n>=20)
    {
        c=c+(n/20);
        n=n%20;
    }
    if(n>=10)
    {
        c=c+(n/10);
        n=n%10;
    }
    if(n>=5)
    {
        c=c+(n/5);
        n=n%5;
    }
    if(n>=2)
    {
        c=c+(n/2);
        n=n%2;
    }
    if(n>=1)
    {
        c=c+(n/1);
    }
    printf("%d",c);
    return 0;
}
